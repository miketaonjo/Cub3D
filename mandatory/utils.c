#include "../includes/cub3D.h"

int	create_rgb(int r, int g, int b)
{
	int	tmp;

	tmp = 0;
	tmp = (r << 16 | g << 8 | b);
	return (tmp);
}

int	ft_isspace(char c)
{
	if (c == 32 || c == '\f' || c == '\n' || c == '\r' || c == '\t'
		|| c == '\v')
		return (0);
	return (1);
}
